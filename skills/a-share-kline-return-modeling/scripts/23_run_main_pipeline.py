#!/usr/bin/env python3
"""Run the reproducible main pipeline for the A-share 5-day model."""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path

import pandas as pd


ROOT = Path(__file__).resolve().parents[3]
SCRIPT_DIR = ROOT / "skills/a-share-kline-return-modeling/scripts"
TEST_DIR = ROOT / "skills/a-share-kline-return-modeling/tests"
EVAL_DIR = ROOT / "skills/a-share-kline-return-modeling/outputs/evaluation"
FINAL_SIGNAL_DIR = ROOT / "skills/a-share-kline-return-modeling/outputs/final_signals"


def run_command(cmd: list[str], enabled: bool = True) -> None:
    if not enabled:
        print(f"skip: {' '.join(cmd)}")
        return
    print(f"\n$ {' '.join(cmd)}")
    subprocess.run(cmd, cwd=ROOT, check=True)


def signal_path(policy: str, start_period: str, end_period: str) -> Path:
    return FINAL_SIGNAL_DIR / f"final_signals_{policy}_{start_period}_{end_period}.csv"


def load_summary(args: argparse.Namespace, policies: list[str]) -> dict[str, object]:
    summary: dict[str, object] = {
        "start_period": args.start_month,
        "end_period": args.end_month,
        "model_mode": args.model_mode,
        "top_n_train": args.top_n_train,
        "top_n_eval": args.top_n_eval,
        "policies": policies,
    }

    walk_forward_path = EVAL_DIR / "walk_forward_summary.csv"
    if walk_forward_path.exists():
        walk_forward = pd.read_csv(walk_forward_path)
        if not walk_forward.empty:
            summary["walk_forward"] = {
                "months": int(len(walk_forward)),
                "avg_model_return": float(walk_forward["model_avg_return"].mean()),
                "avg_model_hit_top30": float(walk_forward["model_hit_top30"].mean()),
                "win_return_vs_random_ratio": float((walk_forward["return_vs_random"] > 0).mean()),
                "win_hit30_vs_random_ratio": float((walk_forward["hit30_vs_random"] > 0).mean()),
            }

    final_backtest_path = EVAL_DIR / "final_signal_backtest" / "final_signal_backtest_summary.csv"
    if final_backtest_path.exists():
        final_backtest = pd.read_csv(final_backtest_path)
        summary["final_signal_backtest"] = final_backtest.to_dict(orient="records")

    return summary


def write_summary(summary: dict[str, object]) -> None:
    EVAL_DIR.mkdir(parents=True, exist_ok=True)
    json_path = EVAL_DIR / "main_pipeline_summary.json"
    md_path = EVAL_DIR / "main_pipeline_summary.md"
    json_path.write_text(json.dumps(summary, ensure_ascii=False, indent=2), encoding="utf-8")

    lines = [
        "# Main Pipeline Summary",
        "",
        "This report is generated by `scripts/23_run_main_pipeline.py`.",
        "",
        "## Scope",
        "",
        f"- period: {summary.get('start_period')} to {summary.get('end_period')}",
        f"- model_mode: {summary.get('model_mode')}",
        f"- top_n_train: {summary.get('top_n_train')}",
        f"- top_n_eval: {summary.get('top_n_eval')}",
        f"- policies: {', '.join(map(str, summary.get('policies', [])))}",
        "",
    ]
    walk_forward = summary.get("walk_forward")
    if isinstance(walk_forward, dict):
        lines.extend(
            [
                "## Walk-forward",
                "",
                f"- months: {walk_forward.get('months')}",
                f"- avg_model_return: {float(walk_forward.get('avg_model_return', 0.0)):.4%}",
                f"- avg_model_hit_top30: {float(walk_forward.get('avg_model_hit_top30', 0.0)):.4f} / 3",
                f"- win_return_vs_random_ratio: {float(walk_forward.get('win_return_vs_random_ratio', 0.0)):.2%}",
                f"- win_hit30_vs_random_ratio: {float(walk_forward.get('win_hit30_vs_random_ratio', 0.0)):.2%}",
                "",
            ]
        )
    final_backtest = summary.get("final_signal_backtest")
    if isinstance(final_backtest, list) and final_backtest:
        final_df = pd.DataFrame(final_backtest)
        keep = [
            col
            for col in [
                "policy",
                "trades",
                "signal_days",
                "avg_size_multiplier",
                "final_equity_curve",
                "total_return",
                "max_drawdown",
                "positive_month_ratio",
                "avg_exposure",
            ]
            if col in final_df.columns
        ]
        lines.extend(["## Final Signal Backtest", "", final_df[keep].to_markdown(index=False, floatfmt=".4f"), ""])

    md_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"\nwrote {json_path}")
    print(f"wrote {md_path}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--start-month", default="2025-05")
    parser.add_argument("--end-month", default="2026-04")
    parser.add_argument("--top-n-train", type=int, default=50)
    parser.add_argument("--top-n-eval", type=int, default=3)
    parser.add_argument("--train-window-days", type=int, default=365)
    parser.add_argument("--model-mode", default="top50_classifier")
    parser.add_argument("--primary-policy", choices=["full_size", "combined_size_v2"], default="full_size")
    parser.add_argument("--include-candidate-policy", action="store_true")
    parser.add_argument("--skip-build-features", action="store_true")
    parser.add_argument("--skip-walk-forward", action="store_true")
    parser.add_argument("--skip-portfolio-backtest", action="store_true")
    parser.add_argument("--skip-tests", action="store_true")
    args = parser.parse_args()

    policies = [args.primary_policy]
    if args.include_candidate_policy and "combined_size_v2" not in policies:
        policies.append("combined_size_v2")

    run_command(
        [sys.executable, str(SCRIPT_DIR / "00_build_features.py")],
        enabled=not args.skip_build_features,
    )
    run_command(
        [
            sys.executable,
            str(SCRIPT_DIR / "05_run_walk_forward.py"),
            "--start-month",
            args.start_month,
            "--end-month",
            args.end_month,
            "--top-n-train",
            str(args.top_n_train),
            "--top-n-eval",
            str(args.top_n_eval),
            "--train-window-days",
            str(args.train_window_days),
            "--model-mode",
            args.model_mode,
        ],
        enabled=not args.skip_walk_forward,
    )
    run_command(
        [
            sys.executable,
            str(SCRIPT_DIR / "14_backtest_portfolio_curve.py"),
            "--start-period",
            args.start_month,
            "--end-period",
            args.end_month,
            "--top-n",
            str(args.top_n_eval),
        ],
        enabled=not args.skip_portfolio_backtest,
    )

    generated_signals: list[Path] = []
    for policy in policies:
        run_command(
            [
                sys.executable,
                str(SCRIPT_DIR / "21_generate_final_signals.py"),
                "--start-period",
                args.start_month,
                "--end-period",
                args.end_month,
                "--top-n",
                str(args.top_n_eval),
                "--position-policy",
                policy,
            ]
        )
        generated_signals.append(signal_path(policy, args.start_month, args.end_month))

    run_command(
        [
            sys.executable,
            str(SCRIPT_DIR / "22_backtest_final_signals.py"),
            *[str(path) for path in generated_signals],
        ]
    )

    run_command(
        [
            sys.executable,
            "-m",
            "py_compile",
            *[str(path) for path in sorted(SCRIPT_DIR.glob("*.py"))],
            *[str(path) for path in sorted(TEST_DIR.glob("*.py"))],
        ],
        enabled=not args.skip_tests,
    )
    run_command(
        [sys.executable, str(TEST_DIR / "test_build_features_contract.py")],
        enabled=not args.skip_tests,
    )
    run_command(
        [sys.executable, str(TEST_DIR / "test_final_signal_contract.py")],
        enabled=not args.skip_tests,
    )

    write_summary(load_summary(args, policies))


if __name__ == "__main__":
    main()
